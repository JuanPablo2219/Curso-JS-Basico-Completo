/* OPERADORES */ // Se utilizan para relacionar, operar esos datos para intentar hacer operaciones con esos datos.

// OPERACIONES ARITMETICAS 
let a = 5;
let b = 10;

console.log(2 + 2);  // SUMA
console.log(6 - 2);  // RESTA
console.log(2 * 2);  // MULTIPLICACION
console.log(2 / 2);  // DIVISION 

console.log(10 % 4); //  MODULO
console.log(a ** b); // EXPONENTE

a++;  // INCREMENTO
console.log(a);

b--;  // DECREMENTO
console.log(b);

// OPERACIONES DE ASIGNACIÒN

let miEdad = 25;  // IGUAL
console.log(miEdad);

miEdad += 2;
console.log(miEdad); // MAS IGUAL

miEdad -= 2;
console.log(miEdad); // MENOS IGUAL

miEdad *= 2;
console.log(miEdad); // MULTIPLICACION IGUAL

miEdad /= 2;
console.log(miEdad); // DIVISION IGUAL

miEdad %= 2;
console.log(miEdad); // MODULO IGUAL

miEdad **= 2;
console.log(miEdad); // EXPONENTE IGUAL

// OPERACIONES DE COMPARACIÒN

let c = 5;
let d = 8;

console.log(c > d); // MAYOR QUE
console.log(c < d); // MENOR QUE
console.log(c >= d); // MAYOR O IGUAL QUE
console.log(c <= d); // MENOR O IGUAL QUE

console.log(c == "6"); // IGUALDAD POR VALOR 
console.log(c == c); // IGUALDAD POR VALOR 

console.log(c === c); // IGUALDAD POR IDENTIDAD (TIPO Y VALOR);
console.log(c === 5); 

console.log(c != 5); // DISTINTO DE 
console.log(c != 6); // DISTINTO DE 

